#include "libft.h"

static int	is_upper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	else
		return (0);
}

int			ft_tolower(int c)
{
	if (is_upper(c))
		c += 'a' - 'A';
	return (c);
}
